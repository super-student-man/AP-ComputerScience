public class PrimitiveTypes {
    public static void main(String[] args) {
        // variable declaration
        int x = 14;
        System.out.println(x);
        // increment
        x ++;
        System.out.println(x);
        // math expression
        x = x + 3;
        System.out.println(x);
        // compound operation
        x -= 3;
        System.out.println(x);
        // type casting
        double y = (double) x;
        System.out.println(y);
        // other useless data types
        long a = 12;
        short b = (short) a;
        byte c = 8; // this is the max value of this useless data type
    }
}
